#!/usr/bin/env python3
"""Generate LaTeX table snippets / macros for paper/ from expanded benchmark CSVs."""

from __future__ import annotations

import json
from pathlib import Path

import numpy as np
import pandas as pd

BENCH = Path(__file__).resolve().parent
RES = BENCH / "results"
PAPER = BENCH.parent / "paper"


def main() -> None:
    dom_path = RES / "benchmark_domains100.csv"
    if dom_path.exists():
        df = pd.read_csv(dom_path)
        mean_r = float(df["rmsd"].mean())
        mean_t = float(df["time_s"].mean())
        med_r = float(df["rmsd"].median())
        n = len(df)
        corr = float(np.corrcoef(df["length"], df["rmsd"])[0, 1]) if n > 2 else float("nan")
        (PAPER / "macros_domains.tex").write_text(
            "% Auto-generated by benchmarks/write_paper_tables.py\n"
            f"\\newcommand{{\\PFDomainN}}{{{n}}}\n"
            f"\\newcommand{{\\PFDomainMeanRMSD}}{{{mean_r:.2f}}}\n"
            f"\\newcommand{{\\PFDomainMedianRMSD}}{{{med_r:.2f}}}\n"
            f"\\newcommand{{\\PFDomainMeanTime}}{{{mean_t:.2f}}}\n"
            f"\\newcommand{{\\PFLengthRMSDCorr}}{{{corr:.2f}}}\n",
            encoding="utf-8",
        )

        class_rows = [
            "\\begin{tabular}{@{}lrrr@{}}\n",
            "  \\toprule\n",
            "  Class & $n$ & Mean RMSD (\\AA) & Mean time (s) \\\\\n",
            "  \\midrule\n",
        ]
        for cls, lab in (
            ("all_alpha", "all-$\\alpha$"),
            ("all_beta", "all-$\\beta$"),
            ("alpha_beta", "$\\alpha/\\beta$"),
        ):
            sub = df[df["class"] == cls]
            if sub.empty:
                continue
            class_rows.append(
                f"  {lab} & {len(sub)} & {sub['rmsd'].mean():.2f} & {sub['time_s'].mean():.2f} \\\\\n"
            )
        class_rows.extend(
            [
                "  \\midrule\n",
                f"  \\textbf{{All}} & \\textbf{{{n}}} & \\textbf{{{mean_r:.2f}}} & \\textbf{{{mean_t:.2f}}} \\\\\n",
                "  \\bottomrule\n",
                "\\end{tabular}\n",
            ]
        )
        (PAPER / "table_class_means.tex").write_text("".join(class_rows), encoding="utf-8")

        pf_t = f"{mean_t:.2f}"
        pf_r = f"{mean_r:.1f}"
    else:
        pf_t, pf_r = "---", "---"

    abl_path = RES / "benchmark_ablation_summary.json"
    if abl_path.exists():
        summary = json.loads(abl_path.read_text(encoding="utf-8"))
        order = [
            ("base_consensus", "Base (consensus only)"),
            ("plus_lookahead", "+ Look-ahead"),
            ("plus_lever", "+ Lever polish"),
            ("plus_ss", "+ SS freezing"),
            ("full_pipeline", "Full local stack"),
        ]
        rows = []
        for key, lab in order:
            if key not in summary:
                rows.append(f"    {lab} & --- & --- \\\\\n")
            else:
                s = summary[key]
                rows.append(
                    f"    {lab} & {s['mean_rmsd']:.2f} & {s['mean_time_s']:.2f} \\\\\n"
                )
        (PAPER / "table_ablation.tex").write_text("".join(rows), encoding="utf-8")

    long_path = RES / "benchmark_long100.csv"
    if long_path.exists():
        ldf = pd.read_csv(long_path)
        (PAPER / "macros_long.tex").write_text(
            "% Auto-generated by benchmarks/write_paper_tables.py\n"
            f"\\newcommand{{\\PFLongN}}{{{len(ldf)}}}\n"
            f"\\newcommand{{\\PFLongMeanTime}}{{{ldf['time_s'].mean():.2f}}}\n"
            f"\\newcommand{{\\PFLongMeanTileRMSD}}{{{ldf['mean_tile_rmsd'].mean():.2f}}}\n"
            f"\\newcommand{{\\PFLongMinLen}}{{{int(ldf['length'].min())}}}\n"
            f"\\newcommand{{\\PFLongMaxLen}}{{{int(ldf['length'].max())}}}\n",
            encoding="utf-8",
        )

    comp = (
        f"    PairFold (this work) & {pf_t} & {pf_r} & Low \\\\\n"
        "    Rosetta ab initio\\footnote{Typical published / community ranges for classical ab~initio on small domains; not re-run here (package not required for PairFold).} & $10^2$--$10^4$ & $\\sim$5--12 & High \\\\\n"
        "    ESMFold\\footnote{Lin et al., Science 2023: single-sequence folding; high VRAM; accuracy typically far closer to native than local fragment assembly on the same small domains.} & $\\sim$10--60 & $\\sim$2--4 & High \\\\\n"
    )
    (PAPER / "table_method_compare.tex").write_text(comp, encoding="utf-8")
    print("Wrote paper macros/tables under", PAPER, flush=True)


if __name__ == "__main__":
    main()
